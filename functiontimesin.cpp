// Default Constructor

FunctionTimeSin::FunctionTimeSin(QObject *parent) : AbstractFunction(parent)
{
   // start timer
   time = new QTime();
   time->start();
}

// ----------------------------------------------------------
//  ####  ###### ##### ##### ###### #####   ####
// #    # #        #     #   #      #    # #
// #      #####    #     #   #####  #    #  ####
