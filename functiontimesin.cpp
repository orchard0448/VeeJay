// #    # #        #     #   #      #   #  #    #
//  ####  ######   #     #   ###### #    #  ####
// ----------------------------------------------------------

// f()
double FunctionTimeSin::f() const
{
   return (qSin(time->elapsed()*0.0062831853*frequency)+1)/2;
}

